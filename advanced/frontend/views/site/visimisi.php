					<h4>A.   Visi GBKP 2016-2020</h4>

<p align="justify">GBKP menjadi kawan sekerja Allah untuk menyatakan rahmat Allah kepada dunia. Dalam Bahasa Inggris: to be God’s fellow-workers to manifest God’s mercy to the world (1 Korintus 3:9 dan I Petrus 2:9-10). Dalam bahasa Karo diartikan sebagai “GBKP aron Dibata guna jadi pasu-pasu man isi doni”.</p>

<br> 

<h4>B.   Misi GBKP 2016-2020</h4>
<ul>
<li>  Menumbuhkembangkan spiritualitas jemaat berbasis Alkitab</li>

<li>  Menegakkan keadilan, perdamaian dan keutuhan ciptaan Allah</li>

<li>  Memperkuat semangat gotong royong antar sesama jemaat dan masyarakat</li>

<li>  Menggali dan menumbuhkembangkan potensi jemaat untuk bersekutu dan bersinergi </li>
</ul>
 
<br>

<h4>C.   Tujuan GBKP </h4>
<p align="justify">
Meningkatnya panggilan dan komitmen warga dalam melakukan pelayanan (berteologi, pendidikan dan kesehatan, berpolitik dan kegiatan ekonomi, pelestarian lingkungan, budaya dan pemanfaatan teknologi informasi) </p>

<br>
            
<h4>
D.   Sasaran GBKP </h4>
<p align="justify">
Sasaran yang ingin dicapai dalam periode 2016-2020 ialah terjadinya transformasi warga GBKP secara efektif dengan tahapan sebagai berikut:
<ul>
<li>Tahun 2016</li>

Meningkatkan komitmen warga GBKP  yang misioner untuk berpartisipasi dalam  pelayanan

<li> Tahun 2017</li>

Meningkatkan kemampuan warga GBKP dalam kehidupan berteologi dan menumbuhkan  motivasi belajar secara mandiri (self learning)

<li>Tahun 2018</li>

Menumbuhkan dan meningkatkan kesadaran warga GBKP dalam bidang politik,  hubungan dengan masyarakat dan pemerintah

<li>Tahun 2019</li>

Meningkatkan jiwa kewirausahaan (enterpreneurship) warga GBKP dalam bidang ekonomi, serta kepedulian terhadap kesehatan dan lingkungan

<li>Tahun 2020</li>

Meningkatkan kemampuan  warga GBKP dalam pengelolaan informasi dan keterampilan dalam  penggunaan teknologi informasi
</ul>
</p>
 
<br>

<h4>E.    Strategi GBKP 2016 - 2020</h4>
<p align="justify">
Strategi yang digunakan untuk mencapai tujuan dan sasaran lima tahun di atas adalah sebagai berikut :
<ul>
<li>Menjalin dan mengefektifkan kerjasama GBKP dengan pusat-pusat pembinaan warga gereja dalam dan luar negeri guna membangun komitmen dalam pelayanan </li> 

<li>Menjalin kerjasama dengan Lembaga Pendidikan Teologi dan lembaga lainnya yang diakui GBKP dalam dan luar negeri untuk  meningkatkan kualitas warga GBKP di bidang teologi dan pendidikan.</li>

<li>Meningkatkan kerja sama dengan lembaga pemerintah, PGI, UEM, WCC, WCRC, Institut Leimena, LSM dan lembaga-lembaga politik, untuk meningkatkan dan mendewasakan warga GBKP dalam berpolitik.</li>

<li>Menjalin kerjasama dengan lembaga-lembaga ekonomi, kesehatan, hukum dan lingkungan hidup.</li>

<li>Menjalin kerjasama dengan Lembaga-lembaga Manajemen Informasi dan Teknologi untuk meningkatkan kemampuan warga gereja dalam pengelolaan informasi dan keterampilan dalam penggunaan teknologi informasi. </li>
</ul>
</p>
